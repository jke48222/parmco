/* ============================================================================
 * CLEANUP
 * ============================================================================ */

/**
 * cleanup — stops the motor, terminates pigpio, and restores the terminal.
 *
 * Called on normal exit ('q' key) and on signal-triggered shutdown.
 * The shutdown sequence is:
 *   1. Set Enable PWM duty to 0 (outputs go high-impedance, motor coasts)
 *   2. Set In1 and In2 both LOW (both low-side transistors off — clean state)
 *   3. Terminate pigpio (releases DMA channel and GPIO mappings)
 *   4. Terminal restoration happens via atexit(restore_terminal)
 */
static void cleanup(void)
{
    printf("\n\n[PARMCO] Shutting down...\n");

    /* Stop the motor by setting Enable PWM duty to 0.
     * With Enable LOW, the L293D output drivers enter high-impedance
     * state and the motor coasts to a stop. */
    gpioPWM(PIN_ENABLE, 0);
    printf("[PARMCO] Motor stopped (Enable PWM duty = 0).\n");

    /* Set both direction inputs LOW — clean idle state.
     * Both In1=LOW and In2=LOW = both low-side transistors engage,
     * shorting the motor terminals through the low-side path = brake.
     * However, since Enable is already LOW (high-Z), the brake has
     * no effect — the motor simply coasts. This is just good practice
     * to leave the pins in a known, safe state. */
    gpio_write(PIN_IN1, 0);
    gpio_write(PIN_IN2, 0);
    printf("[PARMCO] Direction pins cleared (In1=LOW, In2=LOW).\n");

    /* Terminate the pigpio library.
     * This releases the DMA channel, stops all PWM outputs, and frees
     * the memory-mapped register pages used internally by pigpio. */
    gpioTerminate();
    printf("[PARMCO] pigpio terminated.\n");

    /* Terminal restoration happens automatically via atexit(restore_terminal) */
}

