/* --------------------------------------------------------------------------
 * GetManagedObjects — called by BlueZ on the application root path.
 * --------------------------------------------------------------------------
 * BlueZ invokes this to discover the full GATT hierarchy. We must return
 * a dictionary mapping each object path to its interfaces and properties.
 * The response type is: a{oa{sa{sv}}}
 *   - outer dict: object path → interfaces
 *   - middle dict: interface name → properties
 *   - inner dict: property name → value variant
 */
static void handle_get_managed_objects(GDBusConnection       *conn,
                                       const gchar           *sender,
                                       const gchar           *object_path,
                                       const gchar           *interface_name,
                                       const gchar           *method_name,
                                       GVariant              *parameters,
                                       GDBusMethodInvocation *invocation,
                                       gpointer               user_data)
{
    (void)conn; (void)sender; (void)object_path;
    (void)interface_name; (void)method_name; (void)parameters; (void)user_data;

    GVariantBuilder objects;   /* a{oa{sa{sv}}} */
    GVariantBuilder ifaces;    /* a{sa{sv}} */
    GVariantBuilder props;     /* a{sv} */

    g_variant_builder_init(&objects, G_VARIANT_TYPE("a{oa{sa{sv}}}"));

    /* ---- SERVICE object ---- */
    g_variant_builder_init(&ifaces, G_VARIANT_TYPE("a{sa{sv}}"));
    g_variant_builder_init(&props, G_VARIANT_TYPE("a{sv}"));

    /* UUID property: the custom PARMCO service UUID */
    g_variant_builder_add(&props, "{sv}", "UUID",
                          g_variant_new_string(PARMCO_SERVICE_UUID));
    /* Primary: this is a primary service (not included/secondary) */
    g_variant_builder_add(&props, "{sv}", "Primary",
                          g_variant_new_boolean(TRUE));
    /* Characteristics: list of object paths for child characteristics */
    const gchar *char_paths[] = { CMD_CHAR_PATH, TEL_CHAR_PATH, NULL };
    g_variant_builder_add(&props, "{sv}", "Characteristics",
                          g_variant_new_objv(char_paths, -1));

    g_variant_builder_add(&ifaces, "{sa{sv}}", "org.bluez.GattService1",
                          &props);
    g_variant_builder_add(&objects, "{oa{sa{sv}}}", SERVICE_PATH, &ifaces);

    /* ---- COMMAND CHARACTERISTIC object ---- */
    g_variant_builder_init(&ifaces, G_VARIANT_TYPE("a{sa{sv}}"));
    g_variant_builder_init(&props, G_VARIANT_TYPE("a{sv}"));

    g_variant_builder_add(&props, "{sv}", "UUID",
                          g_variant_new_string(COMMAND_CHAR_UUID));
    g_variant_builder_add(&props, "{sv}", "Service",
                          g_variant_new_object_path(SERVICE_PATH));

    /* Flags: "write" + "write-without-response" allows the phone to write
     * command bytes. "write-without-response" avoids triggering SMP pairing
     * on iOS — the ATT Write Without Response path does not require an
     * authenticated link, which prevents the pairing hang. */
    GVariantBuilder cmd_flags;
    g_variant_builder_init(&cmd_flags, G_VARIANT_TYPE("as"));
    g_variant_builder_add(&cmd_flags, "s", "write");
    g_variant_builder_add(&cmd_flags, "s", "write-without-response");
    g_variant_builder_add(&props, "{sv}", "Flags",
                          g_variant_builder_end(&cmd_flags));

    g_variant_builder_add(&ifaces, "{sa{sv}}",
                          "org.bluez.GattCharacteristic1", &props);
    g_variant_builder_add(&objects, "{oa{sa{sv}}}", CMD_CHAR_PATH, &ifaces);

    /* ---- TELEMETRY CHARACTERISTIC object ---- */
    g_variant_builder_init(&ifaces, G_VARIANT_TYPE("a{sa{sv}}"));
    g_variant_builder_init(&props, G_VARIANT_TYPE("a{sv}"));

    g_variant_builder_add(&props, "{sv}", "UUID",
                          g_variant_new_string(TELEMETRY_CHAR_UUID));
    g_variant_builder_add(&props, "{sv}", "Service",
                          g_variant_new_object_path(SERVICE_PATH));

    /* Flags: "read" + "notify" — phone can poll or subscribe to RPM */
    GVariantBuilder tel_flags;
    g_variant_builder_init(&tel_flags, G_VARIANT_TYPE("as"));
    g_variant_builder_add(&tel_flags, "s", "read");
    g_variant_builder_add(&tel_flags, "s", "notify");
    g_variant_builder_add(&props, "{sv}", "Flags",
                          g_variant_builder_end(&tel_flags));

    g_variant_builder_add(&ifaces, "{sa{sv}}",
                          "org.bluez.GattCharacteristic1", &props);
    g_variant_builder_add(&objects, "{oa{sa{sv}}}", TEL_CHAR_PATH, &ifaces);

    /* Return the complete object hierarchy to BlueZ */
    g_dbus_method_invocation_return_value(
        invocation,
        g_variant_new("(a{oa{sa{sv}}})", &objects)
    );
}

