/* --------------------------------------------------------------------------
 * Command characteristic — WriteValue handler
 * --------------------------------------------------------------------------
 * Called by BlueZ when the phone writes bytes to the command characteristic.
 * The first parameter is a byte array (ay) containing the UTF-8 command.
 * The second parameter is an options dict (a{sv}) — unused here.
 */
static void handle_cmd_method_call(GDBusConnection       *conn,
                                   const gchar           *sender,
                                   const gchar           *object_path,
                                   const gchar           *interface_name,
                                   const gchar           *method_name,
                                   GVariant              *parameters,
                                   GDBusMethodInvocation *invocation,
                                   gpointer               user_data)
{
    (void)conn; (void)sender; (void)object_path;
    (void)interface_name; (void)user_data;

    if (g_strcmp0(method_name, "WriteValue") == 0) {
        /* Extract the byte array from the D-Bus message.
         * parameters type is (aya{sv}) — byte array + options dict. */
        GVariant *value_variant = NULL;
        GVariant *options_variant = NULL;
        g_variant_get(parameters, "(@ay@a{sv})", &value_variant, &options_variant);

        /* Get the raw byte data and its length */
        gsize len = 0;
        const guint8 *bytes = g_variant_get_fixed_array(value_variant, &len,
                                                         sizeof(guint8));

        /* Convert byte array to a null-terminated C string.
         * Allocate len+1 bytes to ensure null termination. */
        char *cmd = g_malloc0(len + 1);
        memcpy(cmd, bytes, len);
        cmd[len] = '\0';

        /* Parse and execute the command */
        parse_command(cmd);

        g_free(cmd);
        g_variant_unref(value_variant);
        g_variant_unref(options_variant);

        /* Return success (no output parameters for WriteValue) */
        g_dbus_method_invocation_return_value(invocation, NULL);
    }
}

