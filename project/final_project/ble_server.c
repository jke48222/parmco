/* ============================================================================
 * COMMAND PARSER
 * ============================================================================
 * Decodes a UTF-8 command string received from the phone app and updates
 * the global motor state accordingly. Commands are simple ASCII strings:
 *   "FWD", "REV", "START", "STOP", "SPD:XX"
 */
static void parse_command(const char *cmd)
{
    printf("[PARMCO] Received command: \"%s\"\n", cmd);

    if (strcmp(cmd, "FWD") == 0) {
        /* Set motor direction to forward (clockwise) */
        strncpy(motor_state.direction, "FWD", sizeof(motor_state.direction));
        printf("[PARMCO] Direction → FORWARD\n");

    } else if (strcmp(cmd, "REV") == 0) {
        /* Set motor direction to reverse (counterclockwise) */
        strncpy(motor_state.direction, "REV", sizeof(motor_state.direction));
        printf("[PARMCO] Direction → REVERSE\n");

    } else if (strcmp(cmd, "START") == 0) {
        /* Enable the motor — it will spin at the current speed/direction */
        motor_state.running = 1;
        printf("[PARMCO] Motor → RUNNING\n");

    } else if (strcmp(cmd, "STOP") == 0) {
        /* Disable the motor — it will coast to a stop */
        motor_state.running = 0;
        printf("[PARMCO] Motor → STOPPED\n");

    } else if (strncmp(cmd, "SPD:", 4) == 0) {
        /* Parse the speed value following the "SPD:" prefix */
        int spd = atoi(cmd + 4);
        /* Clamp to valid range [0, 100] */
        if (spd < 0)   spd = 0;
        if (spd > 100)  spd = 100;
        motor_state.speed = spd;
        printf("[PARMCO] Speed → %d%%\n", motor_state.speed);

    /* === HUMAN EDIT START (CP3 Phase 1 F3, Jalen) === */
    } else if (strncmp(cmd, "RPM:", 4) == 0) {
        /* Target-RPM setpoint from the phone. In CP3 this was purely
         * advisory (logged but never acted on) because auto modes were
         * out of scope. In the final build MAINTAIN mode reads this
         * value directly — see update_maintain_mode(). */
        int target = atoi(cmd + 4);
        if (target < TARGET_RPM_MIN) target = TARGET_RPM_MIN;
        if (target > TARGET_RPM_MAX) target = TARGET_RPM_MAX;
        motor_state.target_rpm = target;
        printf("[PARMCO] Target RPM → %d\n", motor_state.target_rpm);
    /* === HUMAN EDIT END === */

    /* === HUMAN EDIT START (Final, Jalen) ===
     * Three mode-change commands complete the phone-side vocabulary:
     *
     *   MODE:MANUAL    → passthrough of SPD:<0-100> to PWM
     *   AUTO:MAINTAIN  → closed loop on motor's own IR, target = RPM:<n>
     *   AUTO:MATCH     → closed loop tracking GPIO23 reference source
     *
     * Each branch just stamps motor_state.mode. The controller tick
     * picks it up on the next 250ms cycle. motor_hw_apply() at the
     * end of this function re-seeds pwm_duty if we're transitioning
     * from MANUAL to an auto mode with the motor already spinning,
     * so the controller doesn't start from zero. */
    } else if (strcmp(cmd, "MODE:MANUAL") == 0) {
        motor_state.mode = MODE_MANUAL;
        /* Drop the controller-owned duty back to whatever the manual
         * speed slider says, so the transition feels instantaneous. */
        motor_state.pwm_duty = (motor_state.speed * PWM_MAX_DUTY) / 100;
        printf("[PARMCO] Mode → MANUAL\n");

    } else if (strcmp(cmd, "AUTO:MAINTAIN") == 0) {
        motor_state.mode = MODE_MAINTAIN;
        printf("[PARMCO] Mode → MAINTAIN (target=%d)\n",
               motor_state.target_rpm);

    } else if (strcmp(cmd, "AUTO:MATCH") == 0) {
        motor_state.mode = MODE_MATCH;
        printf("[PARMCO] Mode → MATCH (ref=%d)\n",
               motor_state.ref_rpm);
    /* === HUMAN EDIT END === */

    } else {
        printf("[PARMCO] Unknown command: \"%s\"\n", cmd);
    }

    /* === HUMAN EDIT START (CP3 Phase 0, Jalen) ===
     * Push the logical motor state out to the L293D pins. Without this call
     * the BLE server just updated a struct — the motor never moved.
     */
    motor_hw_apply();
    /* === HUMAN EDIT END === */
}

