/**
 * update_pwm — sets the PWM duty cycle on the L293D Enable pin.
 *
 * When the motor is off (motor_running == 0), the Enable duty cycle is
 * set to 0. With Enable LOW, the L293D's output drivers enter a
 * high-impedance state — both Output 1 and Output 2 float, and the
 * motor coasts to a stop (no active braking).
 *
 * When the motor is on, the speed percentage (0–100) is linearly mapped
 * to the pigpio duty cycle range (0–255) on the Enable pin:
 *   0%   → duty = 0   (Enable always LOW, outputs high-Z, motor coasts)
 *   50%  → duty = 127 (Enable toggling at 50% duty, ~6V average to motor)
 *   100% → duty = 255 (Enable always HIGH, full 12V across motor)
 *
 * The In1/In2 direction pins remain static during PWM — they set the
 * current path through the H-bridge, while Enable gates the output
 * transistors on and off at the PWM frequency.
 */
static void update_pwm(void)
{
    int duty;

    if (motor_running && motor_speed > 0) {
        /* Map percentage to 0–255 range */
        duty = (motor_speed * PWM_MAX_DUTY) / 100;
    } else {
        duty = 0;
    }

    /* gpioPWM() sets the PWM duty cycle on the Enable pin.
     * pigpio handles the DMA-based timing internally — we just provide
     * the duty cycle value and it maintains the waveform. */
    gpioPWM(PIN_ENABLE, duty);
}

/* ============================================================================
 * DIRECTION UPDATE — L293D H-Bridge Logic
 * ============================================================================ */

/**
 * update_direction — sets the L293D In1/In2 pins for the desired direction.
 *
 * The L293D H-bridge reverses motor polarity by swapping which output
 * is driven HIGH vs LOW. The internal structure contains four transistors
 * (two per output) arranged so that:
 *
 *   In1=HIGH, In2=LOW:
 *     Output 1 sources current (HIGH-side transistor ON)
 *     Output 2 sinks current (LOW-side transistor ON)
 *     Current path: VCC2 → Output 1 → Motor → Output 2 → GND
 *     Result: Motor spins clockwise (FORWARD)
 *
 *   In1=LOW, In2=HIGH:
 *     Output 1 sinks current (LOW-side transistor ON)
 *     Output 2 sources current (HIGH-side transistor ON)
 *     Current path: VCC2 → Output 2 → Motor → Output 1 → GND
 *     Result: Motor spins counterclockwise (REVERSE)
 *
 * The direction can be changed while the motor is spinning. The L293D
 * handles the transition safely — the internal cross-conduction
 * protection prevents shoot-through during switching. However, an
 * instant polarity reversal under full speed subjects the motor and
 * gearbox to high mechanical stress. For smoother reversals, ramp the
 * speed down to 0, change direction, then ramp back up.
 *
 * Uses the assembly gpio_write() function for atomic pin control.
 * gpio_write() accesses GPSET0/GPCLR0 registers which are write-only
 * and bit-atomic — no read-modify-write race condition.
 */
static void update_direction(void)
{
    if (motor_direction) {
        /* FORWARD: In1 = HIGH, In2 = LOW */
        gpio_write(PIN_IN1, 1);
        gpio_write(PIN_IN2, 0);
    } else {
        /* REVERSE: In1 = LOW, In2 = HIGH */
        gpio_write(PIN_IN1, 0);
        gpio_write(PIN_IN2, 1);
    }
}

