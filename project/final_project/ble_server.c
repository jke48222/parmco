/* --------------------------------------------------------------------------
 * Pairing Agent — NoInputNoOutput auto-accept handler
 * --------------------------------------------------------------------------
 * This handler responds to ALL BlueZ Agent1 method calls. The agent's
 * role is to handle the human side of Bluetooth pairing — displaying
 * passkeys, confirming numeric comparisons, entering PINs, etc.
 *
 * With the "NoInputNoOutput" capability, BlueZ tells the remote device
 * (the iPhone) that this device cannot display or input anything. The
 * result is "Just Works" pairing:
 *   - No PIN prompt on either device
 *   - No numeric comparison confirmation
 *   - The link is encrypted but not authenticated (no MITM protection)
 *
 * For PARMCO, this is the correct approach because:
 *   1. We're sending motor commands, not sensitive data
 *   2. The system operates in a controlled lab environment
 *   3. User-interactive pairing is impractical on a headless RP4
 *
 * WHAT WAS HAPPENING WITHOUT THIS AGENT:
 *   When iOS connected and BlueZ initiated SMP pairing (triggered by
 *   the "write" flag on the command characteristic or by iOS probing
 *   standard GATT services), BlueZ dispatched a pairing request to
 *   the registered agent. With NO agent registered, the request sat
 *   in a queue with no handler, producing:
 *     - RP4 side: "Waiting for response from Bluetooth device"
 *     - iPhone side: pairing dialog that eventually times out
 *   Registering this agent ensures every pairing request gets an
 *   immediate "yes" response.
 */
static void handle_agent_method_call(GDBusConnection       *conn,
                                     const gchar           *sender,
                                     const gchar           *object_path,
                                     const gchar           *interface_name,
                                     const gchar           *method_name,
                                     GVariant              *parameters,
                                     GDBusMethodInvocation *invocation,
                                     gpointer               user_data)
{
    (void)conn; (void)sender; (void)object_path;
    (void)interface_name; (void)parameters; (void)user_data;

    if (g_strcmp0(method_name, "RequestConfirmation") == 0) {
        /* Numeric comparison: BlueZ shows a 6-digit number and asks us
         * to confirm it matches what the remote device shows. With
         * NoInputNoOutput, we auto-accept — "Just Works" pairing. */
        printf("[PARMCO] Agent: RequestConfirmation — auto-accepting.\n");
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else if (g_strcmp0(method_name, "RequestAuthorization") == 0) {
        /* BlueZ asks if we authorize this device to connect. Auto-accept. */
        printf("[PARMCO] Agent: RequestAuthorization — auto-accepting.\n");
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else if (g_strcmp0(method_name, "AuthorizeService") == 0) {
        /* BlueZ asks if we authorize access to a specific service UUID.
         * Auto-accept — all services on this device are public. */
        printf("[PARMCO] Agent: AuthorizeService — auto-accepting.\n");
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else if (g_strcmp0(method_name, "RequestPinCode") == 0) {
        /* Legacy PIN code pairing (Bluetooth 2.0 style). Unlikely for
         * BLE, but handle it by returning a default PIN. */
        printf("[PARMCO] Agent: RequestPinCode — returning '0000'.\n");
        g_dbus_method_invocation_return_value(
            invocation, g_variant_new("(s)", "0000"));

    } else if (g_strcmp0(method_name, "RequestPasskey") == 0) {
        /* Passkey entry: return a numeric passkey. Unlikely for
         * NoInputNoOutput capability but handle for completeness. */
        printf("[PARMCO] Agent: RequestPasskey — returning 0.\n");
        g_dbus_method_invocation_return_value(
            invocation, g_variant_new("(u)", (guint32)0));

    } else if (g_strcmp0(method_name, "DisplayPasskey") == 0) {
        /* BlueZ wants us to display a passkey to the user. On a headless
         * device, we just log it. */
        const gchar *device = NULL;
        guint32 passkey = 0;
        guint16 entered = 0;
        g_variant_get(parameters, "(&ouq)", &device, &passkey, &entered);
        printf("[PARMCO] Agent: DisplayPasskey — device=%s passkey=%06u entered=%u\n",
               device, passkey, entered);
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else if (g_strcmp0(method_name, "DisplayPinCode") == 0) {
        /* Display a PIN code. Log it and return. */
        const gchar *device = NULL;
        const gchar *pincode = NULL;
        g_variant_get(parameters, "(&o&s)", &device, &pincode);
        printf("[PARMCO] Agent: DisplayPinCode — device=%s pin=%s\n",
               device, pincode);
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else if (g_strcmp0(method_name, "Release") == 0) {
        /* BlueZ is unregistering this agent. */
        printf("[PARMCO] Agent: Released by BlueZ.\n");
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else if (g_strcmp0(method_name, "Cancel") == 0) {
        /* A pairing operation was cancelled. */
        printf("[PARMCO] Agent: Pairing cancelled.\n");
        g_dbus_method_invocation_return_value(invocation, NULL);

    } else {
        /* Unknown agent method — should never happen */
        printf("[PARMCO] Agent: Unknown method '%s'\n", method_name);
        g_dbus_method_invocation_return_value(invocation, NULL);
    }
}

/* ============================================================================
 * TELEMETRY TIMER CALLBACK
 * ============================================================================
 * Called every TELEMETRY_INTERVAL_MS by the GLib main loop. If a client
 * has subscribed to notifications (telemetry_notifying == TRUE), this emits
 * a PropertiesChanged signal on the telemetry characteristic with the
 * updated RPM value. BlueZ intercepts this signal and pushes the data
 * to the connected phone over the air.
 */
static gboolean telemetry_timer_callback(gpointer user_data)
{
    (void)user_data;

    /* === HUMAN EDIT START (CP3 Phase 1 F2, Jalen) ===
     * Dual-rate cadence: fire every tick (5 Hz) while the motor is running,
     * and skip 4 of every 5 ticks while idle to drop to 1 Hz. */
    static int idle_skip = 0;
    if (!motor_state.running) {
        if (++idle_skip < 5) return G_SOURCE_CONTINUE;
        idle_skip = 0;
    } else {
        idle_skip = 0;
    }
    /* === HUMAN EDIT END === */

    /* === HUMAN EDIT START (Final, Jalen) ===
     * RPM sampling migrated out of here and into controller_timer_callback.
     * This function now just publishes whatever the controller tick last
     * measured — motor_state.rpm and motor_state.ref_rpm. Two reasons:
     *   1. The controller needs the fresh sample first; otherwise MAINTAIN
     *      and MATCH would always be correcting against last cycle's data.
     *   2. Both callbacks hitting motor_hw_sample_rpm() would compete for
     *      the sliding-window static state and desync the window.
     *
     * Idle gating still zeroes the published values so the phone shows 0
     * when the motor is stopped instead of a stale reading. */
    if (!motor_state.running) {
        motor_state.rpm     = 0;
        motor_state.ref_rpm = 0;
    }
    /* === HUMAN EDIT END === */

    /* Only emit notifications if a client is subscribed */
    if (!telemetry_notifying || dbus_conn == NULL)
        return G_SOURCE_CONTINUE;

    /* === HUMAN EDIT START (Final, Jalen) ===
     * Extended payload: "RPM:<actual>,REF:<ref>,M:<mode>"
     *
     * Rationale: three fields the phone needs every tick —
     *   RPM: actual motor RPM (displayed in the big reading)
     *   REF: reference RPM from GPIO23 (displayed when in MATCH mode)
     *   M:   which controller is active (so the UI can echo back what
     *        the phone requested and confirm the RP4 is in sync)
     *
     * ASCII + comma-separated instead of JSON to avoid pulling a JSON
     * library into the Swift BLE parser. Each field keeps its key
     * prefix so fields can appear in any order (and the CP3 parser's
     * "RPM:<int>" prefix-strip still works on the first field).
     *
     * 48 bytes is plenty — the ATT MTU on the iPhone is 185 bytes
     * after negotiation, and a maximal payload here is under 40. */
    const char *mode_str =
        (motor_state.mode == MODE_MAINTAIN) ? "maintain" :
        (motor_state.mode == MODE_MATCH)    ? "match"    : "manual";

    char rpm_str[64];
    snprintf(rpm_str, sizeof(rpm_str),
             "RPM:%d,REF:%d,M:%s",
             motor_state.rpm,
             motor_state.ref_rpm,
             mode_str);
    /* === HUMAN EDIT END === */

    GVariantBuilder value_builder;
    g_variant_builder_init(&value_builder, G_VARIANT_TYPE("ay"));
    for (size_t i = 0; i < strlen(rpm_str); i++) {
        g_variant_builder_add(&value_builder, "y", (guint8)rpm_str[i]);
    }

    /* Build the PropertiesChanged signal arguments:
     *   interface_name: "org.bluez.GattCharacteristic1"
     *   changed_properties: { "Value": <byte array> }
     *   invalidated_properties: [] (empty)
     */
    GVariantBuilder changed;
    g_variant_builder_init(&changed, G_VARIANT_TYPE("a{sv}"));
    g_variant_builder_add(&changed, "{sv}", "Value",
                          g_variant_builder_end(&value_builder));

    GVariantBuilder invalidated;
    g_variant_builder_init(&invalidated, G_VARIANT_TYPE("as"));

    /* Emit the signal on the telemetry characteristic's object path.
     * BlueZ monitors for PropertiesChanged on GattCharacteristic1 objects
     * and forwards the Value change to any subscribed BLE central. */
    GError *err = NULL;
    g_dbus_connection_emit_signal(
        dbus_conn,
        NULL,                                       /* broadcast to all */
        TEL_CHAR_PATH,                              /* object path */
        "org.freedesktop.DBus.Properties",          /* interface */
        "PropertiesChanged",                        /* signal name */
        g_variant_new("(sa{sv}as)",
                      "org.bluez.GattCharacteristic1",
                      &changed,
                      &invalidated),
        &err
    );

    if (err) {
        fprintf(stderr, "[PARMCO] Error emitting telemetry: %s\n", err->message);
        g_error_free(err);
    }

    return G_SOURCE_CONTINUE;  /* Keep the timer running */
}

/* ============================================================================
 * CONTROLLER TIMER CALLBACK                         === HUMAN EDIT (Final) ===
 * ============================================================================
 * Fires every CONTROL_PERIOD_MS regardless of whether a phone is connected.
 * Two jobs:
 *
 *   1. Sample both IR channels into motor_state.rpm and motor_state.ref_rpm
 *      so the telemetry tick (which runs at a separate cadence) has fresh
 *      numbers to publish.
 *
 *   2. If the motor is running AND we're in an auto mode, nudge pwm_duty
 *      by the mode's controller. MANUAL mode bypasses this — PWM in MANUAL
 *      is written synchronously by motor_hw_apply() when SPD: arrives.
 *
 * Running this as a separate GLib source (rather than folding it into the
 * telemetry callback) gives us independent tuning knobs for publish rate
 * and control rate, and keeps the telemetry code from getting tangled up
 * with controller math.
 *
 * The auto modes deliberately keep running through the controller even
 * when motor_state.running == 0 — they just skip the PWM update. This
 * keeps measurement history current so resuming from STOP doesn't start
 * with a stale window.
 */
static gboolean controller_timer_callback(gpointer user_data)
{
    (void)user_data;

    /* Always sample the sensors. Zero out the reading if the motor is
     * stopped rather than trusting the tach (which will show residual
     * coast-down pulses for a second or two after STOP). */
    if (motor_state.running) {
        motor_state.rpm     = motor_hw_sample_rpm();
        motor_state.ref_rpm = motor_hw_sample_ref_rpm();
    } else {
        motor_state.rpm     = 0;
        motor_state.ref_rpm = motor_hw_sample_ref_rpm();
        /* Note we still read the reference even with motor stopped.
         * A bench function generator running continuously is often how
         * we arm MATCH mode BEFORE pressing START on the phone. */
    }

    /* Only the auto modes want controller action. MANUAL's PWM was
     * already written by motor_hw_apply() when SPD: arrived. */
    if (!motor_state.running) return G_SOURCE_CONTINUE;

    switch (motor_state.mode) {
        case MODE_MAINTAIN: update_maintain_mode(); break;
        case MODE_MATCH:    update_match_mode();    break;
        case MODE_MANUAL:
        default:            /* nothing — MANUAL is synchronous */ break;
    }

    return G_SOURCE_CONTINUE;
}
/* === HUMAN EDIT END === */

