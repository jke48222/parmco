static int motor_hw_init(void)
{
    /* Map /dev/gpiomem for the asm helpers (pigpio owns its own mapping) */
    int fd = open("/dev/gpiomem", O_RDWR | O_SYNC);
    if (fd < 0) {
        fprintf(stderr, "[PARMCO] open(/dev/gpiomem) failed — run as root?\n");
        return -1;
    }
    gpio_base_map = mmap(NULL, GPIO_MAP_SIZE, PROT_READ | PROT_WRITE,
                         MAP_SHARED, fd, 0);
    close(fd);
    if (gpio_base_map == MAP_FAILED) {
        fprintf(stderr, "[PARMCO] mmap gpiomem failed\n");
        gpio_base_map = NULL;
        return -1;
    }
    gpio_init(gpio_base_map);

    if (gpioInitialise() < 0) {
        fprintf(stderr, "[PARMCO] gpioInitialise() failed\n");
        return -1;
    }

    /* Direction pins — pigpio owns them so bluez coexistence is clean */
    gpioSetMode(PIN_IN1, PI_OUTPUT);
    gpioSetMode(PIN_IN2, PI_OUTPUT);
    gpioWrite(PIN_IN1, 0);
    gpioWrite(PIN_IN2, 0);

    /* PWM on EN1 — 8-bit range, 1 kHz carrier for the L293D */
    gpioSetMode(PIN_ENABLE, PI_OUTPUT);
    gpioSetPWMrange(PIN_ENABLE, PWM_MAX_DUTY);
    gpioSetPWMfrequency(PIN_ENABLE, 1000);
    gpioPWM(PIN_ENABLE, 0);

    /* IR sensor — pull-up, falling-edge callback */
    gpioSetMode(PIN_IR, PI_INPUT);
    gpioSetPullUpDown(PIN_IR, PI_PUD_UP);
    gpioSetAlertFunc(PIN_IR, ir_edge_cb);

    /* === HUMAN EDIT START (Final, Jalen) ===
     * Reference IR sensor on GPIO23. Pull is set to OFF so an external
     * function generator can drive it without fighting the internal
     * resistor — if a real second IR phototransistor is used instead
     * it provides its own pull-up through the 74HC374 latch. Same
     * bounce filter as the primary via the shared MIN_PULSE_US. */
    gpioSetMode(PIN_IR_REF, PI_INPUT);
    gpioSetPullUpDown(PIN_IR_REF, PI_PUD_OFF);
    gpioSetAlertFunc(PIN_IR_REF, ref_ir_edge_cb);
    /* === HUMAN EDIT END === */

    motor_hw_ready = 1;
    printf("[PARMCO] Motor HW init OK — EN=%d IN1=%d IN2=%d IR=%d IR_REF=%d\n",
           PIN_ENABLE, PIN_IN1, PIN_IN2, PIN_IR, PIN_IR_REF);
    return 0;
}

static void motor_hw_shutdown(void)
{
    if (!motor_hw_ready) return;
    gpioPWM(PIN_ENABLE, 0);
    gpioWrite(PIN_IN1, 0);
    gpioWrite(PIN_IN2, 0);
    gpioSetAlertFunc(PIN_IR, NULL);
    /* === HUMAN EDIT START (Final, Jalen) === */
    gpioSetAlertFunc(PIN_IR_REF, NULL);
    /* === HUMAN EDIT END === */
    gpioTerminate();
    if (gpio_base_map && gpio_base_map != MAP_FAILED) {
        munmap((void *)gpio_base_map, GPIO_MAP_SIZE);
        gpio_base_map = NULL;
    }
    motor_hw_ready = 0;
}

/* Drive the pins to match motor_state. Called from parse_command() whenever
 * a value that affects the output stage has changed.
 *
 * === HUMAN EDIT (Final, Jalen) ===
 * Expanded for mode awareness:
 *   - MANUAL: PWM comes straight from motor_state.speed. This function
 *     writes it.
 *   - MAINTAIN / MATCH: the controller thread owns PWM. This function
 *     only handles direction pins and the running/stopped gate. It
 *     seeds pwm_duty from speed on START so the controller has a
 *     reasonable starting point instead of ramping up from zero.
 */
static void motor_hw_apply(void)
{
    if (!motor_hw_ready) return;

    if (!motor_state.running) {
        gpioPWM(PIN_ENABLE, 0);
        gpioWrite(PIN_IN1, 0);
        gpioWrite(PIN_IN2, 0);
        motor_state.pwm_duty = 0;
        return;
    }

    if (strcmp(motor_state.direction, "FWD") == 0) {
        gpioWrite(PIN_IN1, 1);
        gpioWrite(PIN_IN2, 0);
    } else {
        gpioWrite(PIN_IN1, 0);
        gpioWrite(PIN_IN2, 1);
    }

    if (motor_state.mode == MODE_MANUAL) {
        /* Direct passthrough. speed is 0-100, scale to 0-255. */
        int duty = (motor_state.speed * PWM_MAX_DUTY) / 100;
        if (duty < 0) duty = 0;
        if (duty > PWM_MAX_DUTY) duty = PWM_MAX_DUTY;
        motor_state.pwm_duty = duty;
        gpioPWM(PIN_ENABLE, duty);
    } else {
        /* Auto mode. Seed pwm_duty from speed if the controller hasn't
         * spun up yet, so START doesn't crater to 0 while the loop
         * waits for its first tick. Once pwm_duty is nonzero the
         * controller takes over. */
        if (motor_state.pwm_duty == 0) {
            int seed = (motor_state.speed * PWM_MAX_DUTY) / 100;
            if (seed < 30) seed = 30;       /* stiction floor */
            if (seed > PWM_MAX_DUTY) seed = PWM_MAX_DUTY;
            motor_state.pwm_duty = seed;
        }
        gpioPWM(PIN_ENABLE, motor_state.pwm_duty);
    }
}

/* Snapshot the IR edge counter and compute RPM over RPM_WINDOW_MS. */
static int motor_hw_sample_rpm(void)
{
    static uint32_t window_start_tick = 0;
    static unsigned int last_count = 0;
    static int cached_rpm = 0;

    if (!motor_hw_ready) return 0;

    uint32_t now = gpioTick();
    unsigned int count_now = ir_pulse_count;

    if (window_start_tick == 0) {
        window_start_tick = now;
        last_count = count_now;
        return 0;
    }

    uint32_t elapsed_us = now - window_start_tick;
    if (elapsed_us < (RPM_WINDOW_MS * 1000u)) {
        return cached_rpm;
    }

    unsigned int pulses = count_now - last_count;
    /* rpm = pulses per minute / pulses per rev */
    cached_rpm = (int)((pulses * 60000000ull) /
                       ((uint64_t)elapsed_us * PULSES_PER_REV));
    window_start_tick = now;
    last_count = count_now;
    return cached_rpm;
}
