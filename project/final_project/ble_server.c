/* ============================================================================
 * REGISTRATION HELPERS
 * ============================================================================
 * These functions register each D-Bus object with the system bus using the
 * introspection XML and vtable defined above.
 */

/**
 * register_object — register a single D-Bus object at the given path.
 *
 * @param conn         The D-Bus connection
 * @param path         Object path (e.g., "/com/parmco/service0")
 * @param xml          Introspection XML defining the interface
 * @param vtable       Method call and property handler dispatch table
 * @param description  Human-readable name for error messages
 * @return             Registration ID (nonzero on success, 0 on failure)
 */
static guint register_object(GDBusConnection             *conn,
                              const gchar                 *path,
                              const gchar                 *xml,
                              const GDBusInterfaceVTable  *vtable,
                              const char                  *description)
{
    GError *err = NULL;

    /* Parse the introspection XML into a GDBusNodeInfo structure */
    GDBusNodeInfo *node_info = g_dbus_node_info_new_for_xml(xml, &err);
    if (err) {
        fprintf(stderr, "[PARMCO] Failed to parse %s XML: %s\n",
                description, err->message);
        g_error_free(err);
        return 0;
    }

    /* Register the object on the bus. BlueZ will discover it via
     * GetManagedObjects or by probing the object path directly. */
    guint reg_id = g_dbus_connection_register_object(
        conn,
        path,
        node_info->interfaces[0],  /* First (and only) interface in the XML */
        vtable,
        NULL,   /* user_data — not needed; we use globals */
        NULL,   /* GDestroyNotify — no cleanup needed */
        &err
    );

    if (err) {
        fprintf(stderr, "[PARMCO] Failed to register %s: %s\n",
                description, err->message);
        g_error_free(err);
        g_dbus_node_info_unref(node_info);
        return 0;
    }

    printf("[PARMCO] Registered %s at %s\n", description, path);

    /* Note: node_info is intentionally NOT unreffed here.
     * GDBus holds a reference to the interface info internally and requires
     * it to remain valid for the lifetime of the registration. Freeing it
     * here would cause use-after-free crashes on the first method call.
     * It will be freed when the program exits. */

    return reg_id;
}

/* ============================================================================
 * BLUEZ REGISTRATION CALLBACKS
 * ============================================================================
 * Called when BlueZ responds to our RegisterApplication / RegisterAdvertisement
 * D-Bus method calls. On success, the GATT server or advertisement is live.
 */

static void on_app_registered(GObject *source, GAsyncResult *res, gpointer data)
{
    (void)data;
    GError *err = NULL;
    GVariant *result = g_dbus_connection_call_finish(
        G_DBUS_CONNECTION(source), res, &err);

    if (err) {
        fprintf(stderr, "[PARMCO] Failed to register GATT application: %s\n",
                err->message);
        g_error_free(err);
        return;
    }

    printf("[PARMCO] GATT application registered with BlueZ.\n");
    if (result) g_variant_unref(result);
}

static void on_adv_registered(GObject *source, GAsyncResult *res, gpointer data)
{
    (void)data;
    GError *err = NULL;
    GVariant *result = g_dbus_connection_call_finish(
        G_DBUS_CONNECTION(source), res, &err);

    if (err) {
        fprintf(stderr, "[PARMCO] Failed to register advertisement: %s\n",
                err->message);
        g_error_free(err);
        return;
    }

    printf("[PARMCO] BLE advertisement registered. Discoverable as \"PARMCO-RP4\".\n");
    if (result) g_variant_unref(result);
}

/* ============================================================================
 * SIGNAL HANDLER — Clean shutdown
 * ============================================================================
 * Catches SIGINT (Ctrl+C) and SIGTERM to gracefully stop the main loop,
 * which triggers cleanup in main(). Without this, the advertisement and
 * GATT service would remain registered in BlueZ until reboot.
 */
static void signal_handler(int signum)
{
    printf("\n[PARMCO] Caught signal %d, shutting down...\n", signum);
    if (main_loop && g_main_loop_is_running(main_loop)) {
        g_main_loop_quit(main_loop);
    }
}

/* ============================================================================
 * MAIN
 * ============================================================================ */
int main(int argc, char *argv[])
{
    (void)argc; (void)argv;

    GError *err = NULL;

    printf("============================================================\n");
    printf("  PARMCO BLE Server — ECSE 4235\n");
    printf("  University of Georgia\n");
    printf("  Justin Moreno & Jalen Edusei\n");
    printf("============================================================\n\n");

    /* --- Install signal handlers for clean shutdown --- */
    signal(SIGINT,  signal_handler);
    signal(SIGTERM, signal_handler);

    /* === HUMAN EDIT START (CP3 Phase 0, Jalen) ===
     * Bring up the motor hardware before BlueZ so the first command that
     * arrives has pins to drive. If this fails we still run the BLE side —
     * the grader at least sees advertising and logs, and we print a clear
     * error so the bench test isn't mysterious. */
    if (motor_hw_init() < 0) {
        fprintf(stderr, "[PARMCO] WARNING — motor HW init failed; "
                        "BLE will run in log-only mode.\n");
    }
    /* === HUMAN EDIT END === */

    /* --- Connect to the system D-Bus ---
     * All BlueZ communication happens on the system bus, not the session bus.
     * The program must either run as root or have appropriate D-Bus policy
     * permissions (see parmco-dbus.conf). */
    dbus_conn = g_bus_get_sync(G_BUS_TYPE_SYSTEM, NULL, &err);
    if (err) {
        fprintf(stderr, "[PARMCO] Cannot connect to system D-Bus: %s\n",
                err->message);
        g_error_free(err);
        return EXIT_FAILURE;
    }
    printf("[PARMCO] Connected to system D-Bus.\n");

    /* =================================================================
     * PAIRING AGENT REGISTRATION — must happen BEFORE GATT registration
     * =================================================================
     * The agent must be registered and set as default BEFORE we register
     * the GATT application and start advertising. If a phone connects
     * before the agent is ready, BlueZ has no handler for the pairing
     * request and will hang with "Waiting for response."
     *
     * Registration sequence:
     *   1. Register the agent D-Bus object at AGENT_PATH
     *   2. Call AgentManager1.RegisterAgent() to tell BlueZ about it
     *   3. Call AgentManager1.RequestDefaultAgent() to make it the
     *      system-wide default for all pairing requests
     */

    /* Step 1: Register the agent D-Bus object */
    agent_reg_id = register_object(dbus_conn, AGENT_PATH,
                                   agent_introspection_xml, &agent_vtable,
                                   "Pairing Agent (NoInputNoOutput)");
    if (!agent_reg_id) {
        fprintf(stderr, "[PARMCO] Failed to register pairing agent object.\n");
        return EXIT_FAILURE;
    }

    /* Step 2: Register the agent with BlueZ's AgentManager1.
     * Arguments: (object_path, capability_string)
     * The capability "NoInputNoOutput" tells BlueZ to use Just Works pairing. */
    printf("[PARMCO] Registering pairing agent with BlueZ...\n");
    g_dbus_connection_call_sync(
        dbus_conn,
        BLUEZ_BUS_NAME,
        "/org/bluez",              /* AgentManager1 lives on /org/bluez */
        AGENT_MANAGER_IFACE,
        "RegisterAgent",
        g_variant_new("(os)", AGENT_PATH, AGENT_CAPABILITY),
        NULL,
        G_DBUS_CALL_FLAGS_NONE,
        -1, NULL, &err
    );
    if (err) {
        fprintf(stderr, "[PARMCO] Failed to register agent: %s\n", err->message);
        fprintf(stderr, "[PARMCO] Pairing may fail — continuing anyway.\n");
        g_error_free(err);
        err = NULL;
    } else {
        printf("[PARMCO] Pairing agent registered (capability: %s).\n",
               AGENT_CAPABILITY);
    }

    /* Step 3: Set this agent as the default agent for ALL pairing requests.
     * Without this, BlueZ might use a different agent (e.g., from
     * bluetoothctl or a desktop Bluetooth manager) or no agent at all. */
    g_dbus_connection_call_sync(
        dbus_conn,
        BLUEZ_BUS_NAME,
        "/org/bluez",
        AGENT_MANAGER_IFACE,
        "RequestDefaultAgent",
        g_variant_new("(o)", AGENT_PATH),
        NULL,
        G_DBUS_CALL_FLAGS_NONE,
        -1, NULL, &err
    );
    if (err) {
        fprintf(stderr, "[PARMCO] Failed to set default agent: %s\n", err->message);
        g_error_free(err);
        err = NULL;
    } else {
        printf("[PARMCO] Pairing agent set as default.\n");
    }

    /* --- Register all GATT objects on the bus --- */
    app_reg_id = register_object(dbus_conn, APP_PATH,
                                 app_introspection_xml, &app_vtable,
                                 "Application (ObjectManager)");

    svc_reg_id = register_object(dbus_conn, SERVICE_PATH,
                                 service_introspection_xml, &service_vtable,
                                 "GATT Service");

    cmd_reg_id = register_object(dbus_conn, CMD_CHAR_PATH,
                                 cmd_char_introspection_xml, &cmd_char_vtable,
                                 "Command Characteristic");

    tel_reg_id = register_object(dbus_conn, TEL_CHAR_PATH,
                                 tel_char_introspection_xml, &tel_char_vtable,
                                 "Telemetry Characteristic");

    adv_reg_id = register_object(dbus_conn, ADV_PATH,
                                 adv_introspection_xml, &adv_vtable,
                                 "LE Advertisement");

    if (!app_reg_id || !svc_reg_id || !cmd_reg_id || !tel_reg_id || !adv_reg_id) {
        fprintf(stderr, "[PARMCO] One or more objects failed to register. Exiting.\n");
        return EXIT_FAILURE;
    }

    /* --- Register the GATT application with BlueZ ---
     * This tells BlueZ's GattManager1 to inspect our ObjectManager at APP_PATH
     * and incorporate our service/characteristics into its GATT database. */
    printf("[PARMCO] Registering GATT application with BlueZ...\n");
    g_dbus_connection_call(
        dbus_conn,
        BLUEZ_BUS_NAME,                         /* destination: BlueZ daemon */
        ADAPTER_PATH,                           /* object: the BT adapter */
        GATT_MANAGER_IFACE,                     /* interface */
        "RegisterApplication",                  /* method */
        g_variant_new("(oa{sv})",               /* arguments: (object_path, options) */
                      APP_PATH,
                      NULL),                    /* no options */
        NULL,                                   /* no expected return type */
        G_DBUS_CALL_FLAGS_NONE,
        -1,                                     /* default timeout */
        NULL,                                   /* no cancellable */
        on_app_registered,                      /* async callback */
        NULL                                    /* callback user_data */
    );

    /* --- Register the BLE advertisement with BlueZ ---
     * This tells BlueZ's LEAdvertisingManager1 to broadcast our advertisement
     * packets containing the PARMCO service UUID and local name. */
    printf("[PARMCO] Registering BLE advertisement...\n");
    g_dbus_connection_call(
        dbus_conn,
        BLUEZ_BUS_NAME,
        ADAPTER_PATH,
        LE_ADV_MANAGER_IFACE,
        "RegisterAdvertisement",
        g_variant_new("(oa{sv})",
                      ADV_PATH,
                      NULL),
        NULL,
        G_DBUS_CALL_FLAGS_NONE,
        -1,
        NULL,
        on_adv_registered,
        NULL
    );

    /* --- Start periodic telemetry updates ---
     * Every 500ms, the timer callback updates the mock RPM value and, if a
     * client is subscribed, emits a PropertiesChanged signal to push the
     * new value over BLE. */
    telemetry_timer_id = g_timeout_add(TELEMETRY_INTERVAL_MS,
                                        telemetry_timer_callback, NULL);

    /* === HUMAN EDIT START (Final, Jalen) ===
     * Controller timer. Runs at CONTROL_PERIOD_MS (250 ms) and handles
     * sensor sampling + the MAINTAIN/MATCH proportional controllers.
     * Independent of the telemetry timer so the two cadences can be
     * tuned separately — controller is fast enough to feel responsive,
     * telemetry is fast enough to feel live, neither dominates the BLE
     * notification queue. */
    controller_timer_id = g_timeout_add(CONTROL_PERIOD_MS,
                                        controller_timer_callback, NULL);
    /* === HUMAN EDIT END === */

    /* --- Enter the GLib main event loop ---
     * This loop processes all D-Bus method calls, signals, and timer events.
     * It runs until g_main_loop_quit() is called (by the signal handler). */
    printf("\n[PARMCO] PARMCO BLE Server started. Advertising...\n");
    printf("[PARMCO] Waiting for phone connection. Press Ctrl+C to stop.\n\n");

    printf("[PARMCO] Motor state: direction=%s speed=%d%% running=%s rpm=%d\n\n",
           motor_state.direction,
           motor_state.speed,
           motor_state.running ? "YES" : "NO",
           motor_state.rpm);

    main_loop = g_main_loop_new(NULL, FALSE);
    g_main_loop_run(main_loop);

    /* =====================================================================
     * CLEANUP — reached after signal handler calls g_main_loop_quit()
     * ===================================================================== */

    printf("[PARMCO] Cleaning up...\n");

    /* === HUMAN EDIT START (CP3 Phase 0, Jalen) ===
     * Kill motor power FIRST so a hung BlueZ unregister can't leave the
     * L293D sinking current into a stalled motor. */
    motor_hw_shutdown();
    /* === HUMAN EDIT END === */

    /* Remove the telemetry timer */
    if (telemetry_timer_id > 0) {
        g_source_remove(telemetry_timer_id);
        telemetry_timer_id = 0;
    }

    /* === HUMAN EDIT START (Final, Jalen) ===
     * Remove the controller timer. Ordering matters — timers before
     * D-Bus unregister so no callback fires into a torn-down connection. */
    if (controller_timer_id > 0) {
        g_source_remove(controller_timer_id);
        controller_timer_id = 0;
    }
    /* === HUMAN EDIT END === */

    /* Unregister the advertisement from BlueZ */
    if (dbus_conn) {
        g_dbus_connection_call_sync(
            dbus_conn,
            BLUEZ_BUS_NAME,
            ADAPTER_PATH,
            LE_ADV_MANAGER_IFACE,
            "UnregisterAdvertisement",
            g_variant_new("(o)", ADV_PATH),
            NULL,
            G_DBUS_CALL_FLAGS_NONE,
            -1, NULL, NULL
        );
        printf("[PARMCO] Advertisement unregistered.\n");
    }

    /* Unregister the GATT application from BlueZ */
    if (dbus_conn) {
        g_dbus_connection_call_sync(
            dbus_conn,
            BLUEZ_BUS_NAME,
            ADAPTER_PATH,
            GATT_MANAGER_IFACE,
            "UnregisterApplication",
            g_variant_new("(o)", APP_PATH),
            NULL,
            G_DBUS_CALL_FLAGS_NONE,
            -1, NULL, NULL
        );
        printf("[PARMCO] GATT application unregistered.\n");
    }

    /* Unregister the pairing agent from BlueZ */
    if (dbus_conn) {
        g_dbus_connection_call_sync(
            dbus_conn,
            BLUEZ_BUS_NAME,
            "/org/bluez",
            AGENT_MANAGER_IFACE,
            "UnregisterAgent",
            g_variant_new("(o)", AGENT_PATH),
            NULL,
            G_DBUS_CALL_FLAGS_NONE,
            -1, NULL, NULL
        );
        printf("[PARMCO] Pairing agent unregistered.\n");
    }

    /* Unregister D-Bus objects */
    if (agent_reg_id) g_dbus_connection_unregister_object(dbus_conn, agent_reg_id);
    if (adv_reg_id) g_dbus_connection_unregister_object(dbus_conn, adv_reg_id);
    if (tel_reg_id) g_dbus_connection_unregister_object(dbus_conn, tel_reg_id);
    if (cmd_reg_id) g_dbus_connection_unregister_object(dbus_conn, cmd_reg_id);
    if (svc_reg_id) g_dbus_connection_unregister_object(dbus_conn, svc_reg_id);
    if (app_reg_id) g_dbus_connection_unregister_object(dbus_conn, app_reg_id);

    /* Free the main loop */
    if (main_loop) g_main_loop_unref(main_loop);

    printf("[PARMCO] Shutdown complete.\n");
    return EXIT_SUCCESS;
}
