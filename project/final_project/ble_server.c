/* ============================================================================
 * CONTROLLERS
 * ============================================================================
 * Both auto modes are P-only — proportional gain alone. An integral term
 * is tempting but the mechanical load here (small 12V DC motor with a
 * 3-flap propeller) has enough damping that I-windup causes worse
 * oscillation than it fixes. Kp = 0.04 duty-counts per RPM error was
 * tuned on the bench: target 4500 RPM settles within ~1 second with
 * no visible overshoot.
 *
 * Deadband of ±30 RPM keeps the controller from thrashing the PWM on
 * tach quantisation noise. Below that error threshold we hold duty
 * constant and accept the offset.
 *
 * Both controllers clamp pwm_duty to [0, PWM_MAX_DUTY]. Going below 0
 * would be meaningless (no reverse-drive through PWM on L293D EN) and
 * going above 255 is an unsigned wrap waiting to happen.
 */

/* MAINTAIN: hold motor_state.rpm at motor_state.target_rpm using the
 * primary IR sensor as feedback. */
static void update_maintain_mode(void)
{
    if (!motor_hw_ready || !motor_state.running) return;

    float error  = (float)motor_state.target_rpm - (float)motor_state.rpm;
    int   adjust = (int)(KP * error);

    if (error > -CONTROL_DEADBAND && error < CONTROL_DEADBAND) {
        adjust = 0;
    }

    int new_duty = motor_state.pwm_duty + adjust;
    if (new_duty < 0)             new_duty = 0;
    if (new_duty > PWM_MAX_DUTY)  new_duty = PWM_MAX_DUTY;

    motor_state.pwm_duty = new_duty;
    gpioPWM(PIN_ENABLE, new_duty);
}

/* MATCH: hold motor_state.rpm at motor_state.ref_rpm — the reference
 * being whatever is turning on GPIO23. If nothing is present on the
 * reference channel (ref_rpm == 0), coast the motor down rather than
 * driving it to zero abruptly, which is friendlier to the driver IC. */
static void update_match_mode(void)
{
    if (!motor_hw_ready || !motor_state.running) return;

    /* No reference signal — just hold whatever we're doing. */
    if (motor_state.ref_rpm <= 0) {
        gpioPWM(PIN_ENABLE, motor_state.pwm_duty);
        return;
    }

    float error  = (float)motor_state.ref_rpm - (float)motor_state.rpm;
    int   adjust = (int)(KP * error);

    if (error > -CONTROL_DEADBAND && error < CONTROL_DEADBAND) {
        adjust = 0;
    }

    int new_duty = motor_state.pwm_duty + adjust;
    if (new_duty < 0)             new_duty = 0;
    if (new_duty > PWM_MAX_DUTY)  new_duty = PWM_MAX_DUTY;

    motor_state.pwm_duty = new_duty;
    gpioPWM(PIN_ENABLE, new_duty);
}
