/**
 * restore_terminal — restores the terminal to its original settings.
 *
 * Called automatically by atexit() when the program exits (normally or
 * via exit()). This is CRITICAL: if the terminal is left in raw mode,
 * the user's shell becomes unusable (no echo, no line editing, no Ctrl+C).
 */
static void restore_terminal(void)
{
    if (termios_saved) {
        /* TCSANOW applies the change immediately, without waiting for
         * pending output to drain. This ensures cleanup even on crashes. */
        tcsetattr(STDIN_FILENO, TCSANOW, &orig_termios);
        termios_saved = 0;
        printf("\n[PARMCO] Terminal restored.\n");
    }
}

/**
 * set_raw_mode — switches the terminal to raw, non-echoing mode.
 *
 * In raw mode:
 *   - Characters are available to read() immediately (no Enter needed)
 *   - Typed characters are not echoed to the screen
 *   - Special characters (Ctrl+C, Ctrl+Z) are not processed by the terminal
 *
 * This is necessary for real-time keyboard control of the motor. In
 * canonical mode (the default), the terminal buffers entire lines and
 * only delivers them when the user presses Enter — completely unusable
 * for interactive motor control.
 */
static void set_raw_mode(void)
{
    struct termios raw;

    /* Save the current terminal settings so we can restore them later.
     * tcgetattr() populates the termios struct with the current config. */
    if (tcgetattr(STDIN_FILENO, &orig_termios) < 0) {
        perror("[PARMCO] tcgetattr failed");
        exit(EXIT_FAILURE);
    }
    termios_saved = 1;

    /* Register the restore function to run on ANY exit path.
     * atexit() handlers run for normal exit(), return from main(), and
     * some abnormal terminations. Signal handlers cover SIGINT/SIGTERM. */
    atexit(restore_terminal);

    /* Copy the original settings and modify the copy. */
    raw = orig_termios;

    /* c_lflag controls "local" (terminal-side) processing.
     *
     * ICANON (canonical mode): when set, the terminal buffers input
     *   line-by-line and only delivers complete lines to read().
     *   Clearing it enables character-at-a-time input.
     *
     * ECHO: when set, typed characters are displayed on screen.
     *   Clearing it prevents keystrokes from cluttering our status output.
     *
     * The ~(ICANON | ECHO) expression creates a bitmask with those two
     * bits cleared, and &= applies it without affecting other flags. */
    raw.c_lflag &= ~(ICANON | ECHO);

    /* c_cc[VMIN] = minimum number of characters for read() to return.
     * Setting it to 0 means read() returns immediately even if no data
     * is available (non-blocking when combined with select()). */
    raw.c_cc[VMIN] = 0;

    /* c_cc[VTIME] = read timeout in tenths of a second.
     * Setting it to 0 means no timeout — read() returns immediately. */
    raw.c_cc[VTIME] = 0;

    /* Apply the modified settings immediately (TCSANOW). */
    if (tcsetattr(STDIN_FILENO, TCSANOW, &raw) < 0) {
        perror("[PARMCO] tcsetattr failed");
        exit(EXIT_FAILURE);
    }
}

/**
 * kbhit — checks if a keypress is waiting in the input buffer.
 *
 * Uses the select() system call with a zero timeout to poll the stdin
 * file descriptor without blocking. select() examines a set of file
 * descriptors and reports which ones are ready for I/O.
 *
 * With a zero timeout (both tv_sec and tv_usec = 0), select() checks
 * the buffer state and returns immediately:
 *   - Returns > 0 if data is available to read
 *   - Returns 0 if the buffer is empty
 *   - Returns -1 on error
 *
 * @return 1 if a key is available, 0 if not.
 */
static int kbhit(void)
{
    fd_set read_fds;
    struct timeval timeout;

    /* Zero the file descriptor set, then add stdin (fd 0) to it.
     * FD_ZERO clears all bits. FD_SET sets the bit for STDIN_FILENO. */
    FD_ZERO(&read_fds);
    FD_SET(STDIN_FILENO, &read_fds);

    /* Set timeout to zero — makes select() non-blocking.
     * It will check the buffer and return immediately. */
    timeout.tv_sec = 0;
    timeout.tv_usec = 0;

    /* select() arguments:
     *   nfds = STDIN_FILENO + 1 (one past the highest fd in the set)
     *   readfds = our set containing stdin
     *   writefds = NULL (we don't care about write readiness)
     *   exceptfds = NULL (we don't care about exceptions)
     *   timeout = 0 seconds, 0 microseconds (poll and return) */
    return select(STDIN_FILENO + 1, &read_fds, NULL, NULL, &timeout) > 0;
}

/**
 * read_key — reads a single character from stdin.
 *
 * Should only be called after kbhit() returns 1, guaranteeing that
 * data is available. In raw mode with VMIN=0, read() will return
 * immediately with 0 bytes if the buffer is empty.
 *
 * @return the character read, or -1 if read failed.
 */
static int read_key(void)
{
    char c;
    if (read(STDIN_FILENO, &c, 1) == 1) {
        return (int)c;
    }
    return -1;
}

/* ============================================================================
 * GPIO MEMORY MAPPING
 * ============================================================================ */

/**
 * map_gpio — maps the BCM2711 GPIO registers into user-space virtual memory.
 *
 * The BCM2711's GPIO control registers exist at physical address 0xFE200000.
 * User-space programs cannot access physical memory directly — the MMU
 * translates virtual addresses to physical addresses, and kernel-reserved
 * physical pages are not mapped into user-space page tables.
 *
 * /dev/gpiomem is a character device that allows user-space programs to
 * mmap() the GPIO register page specifically. Unlike /dev/mem (which maps
 * ALL physical memory and requires root), /dev/gpiomem maps only the GPIO
 * block and can be used by non-root users who are in the 'gpio' group.
 * However, since we also use pigpio (which requires root for DMA access),
 * we run as root anyway.
 *
 * mmap() returns a pointer to the virtual address where the GPIO registers
 * are mapped. This pointer is passed to our assembly functions via gpio_init().
 *
 * @return a volatile pointer to the mapped GPIO registers, or exits on failure.
 */
static volatile void *map_gpio(void)
{
    int fd;
    volatile void *gpio_map;

    /* Open the GPIO memory device.
     * O_RDWR = read/write access (we need to write to GPFSEL, GPSET, GPCLR).
     * O_SYNC = disable caching (GPIO registers must not be cached — writes
     *          must go directly to the hardware). */
    fd = open("/dev/gpiomem", O_RDWR | O_SYNC);
    if (fd < 0) {
        perror("[PARMCO] Failed to open /dev/gpiomem");
        fprintf(stderr, "Make sure you are running as root (sudo).\n");
        exit(EXIT_FAILURE);
    }

    /* Map the GPIO register page into our virtual address space.
     *
     * Arguments:
     *   NULL       — let the kernel choose the virtual address
     *   GPIO_MAP_SIZE — map 4096 bytes (one page, covers all GPIO registers)
     *   PROT_READ | PROT_WRITE — we need both read and write access
     *   MAP_SHARED — changes are visible to other processes and to hardware
     *   fd         — the /dev/gpiomem file descriptor
     *   0          — offset within /dev/gpiomem (0 = start of GPIO block)
     *
     * Returns MAP_FAILED on error. */
    gpio_map = mmap(NULL, GPIO_MAP_SIZE, PROT_READ | PROT_WRITE,
                    MAP_SHARED, fd, 0);

    /* Close the file descriptor — the mapping persists after close(). */
    close(fd);

    if (gpio_map == MAP_FAILED) {
        perror("[PARMCO] mmap failed");
        exit(EXIT_FAILURE);
    }

    return gpio_map;
}

