static volatile void *gpio_base_map = NULL;
static int            motor_hw_ready = 0;

/* IR edge state — updated from a pigpio alert callback */
static volatile unsigned int ir_pulse_count = 0;
static volatile uint32_t     ir_last_tick   = 0;

/* === HUMAN EDIT START (Final, Jalen) ===
 * Reference IR state lives alongside the primary tach state. Same
 * debounce rule, same edge polarity, different pin. Volatile because
 * the pigpio alert thread touches these asynchronously to the main
 * GLib loop. */
static volatile unsigned int ref_pulse_count = 0;
static volatile uint32_t     ref_last_tick   = 0;
/* === HUMAN EDIT END === */

static void ir_edge_cb(int gpio, int level, uint32_t tick)
{
    (void)gpio;
    /* falling edge only, with bounce filter */
    if (level != 0) return;
    if (ir_last_tick != 0 && (tick - ir_last_tick) < MIN_PULSE_US) return;
    ir_last_tick = tick;
    ir_pulse_count++;
}

/* === HUMAN EDIT START (Final, Jalen) ===
 * Reference IR callback. Mirrors ir_edge_cb exactly. Separate function
 * because pigpio wants one callback per pin — we can't multiplex. */
static void ref_ir_edge_cb(int gpio, int level, uint32_t tick)
{
    (void)gpio;
    if (level != 0) return;
    if (ref_last_tick != 0 && (tick - ref_last_tick) < MIN_PULSE_US) return;
    ref_last_tick = tick;
    ref_pulse_count++;
}
